import Footer from '../../components/footer/footer';
import Logo from '../../components/logo/logo';
import {ReactElement} from 'react';
import FilmsList from '../../components/films-list/films-list';
import {Film} from '../../types/film-type';
import Tabs from '../../components/tabs/tabs';
import {useParams} from 'react-router-dom';

const SIMILAR_FILMS_COUNT = 4;

function MoviePage(films : Film[]): ReactElement {
  const params = useParams();
  const id = Number(params.id) ?? 1;
  const film : Film = films[id - 1];
  const similarFilms : Film[] = [];
  for (let i = 0; i < SIMILAR_FILMS_COUNT; i++) {
    similarFilms.push(films[i]);
  }
  return (
    <>
      <section className="film-card film-card--full">
        <div className="film-card__hero">
          <div className="film-card__bg">
            <img src={film.src} alt={film.title}/>
          </div>

          <h1 className="visually-hidden">WTW</h1>

          <header className="page-header film-card__head">
            <Logo />

            <ul className="user-block">
              <li className="user-block__item">
                <div className="user-block__avatar">
                  <img src="img/avatar.jpg" alt="User avatar" width="63" height="63"/>
                </div>
              </li>
              <li className="user-block__item">
                <a className="user-block__link">Sign out</a>
              </li>
            </ul>
          </header>

          <div className="film-card__wrap">
            <div className="film-card__desc">
              <h2 className="film-card__title">The Grand Budapest Hotel</h2>
              <p className="film-card__meta">
                <span className="film-card__genre">Drama</span>
                <span className="film-card__year">2014</span>
              </p>

              <div className="film-card__buttons">
                <button className="btn btn--play film-card__button" type="button">
                  <svg viewBox="0 0 19 19" width="19" height="19">
                    <use xlinkHref="#play-s"></use>
                  </svg>
                  <span>Play</span>
                </button>
                <button className="btn btn--list film-card__button" type="button">
                  <svg viewBox="0 0 19 20" width="19" height="20">
                    <use xlinkHref="#add"></use>
                  </svg>
                  <span>My list</span>
                  <span className="film-card__count">9</span>
                </button>
                <a href="add-review.html" className="btn film-card__button">Add review</a>
              </div>
            </div>
          </div>
        </div>

        <div className="film-card__wrap film-card__translate-top">
          <div className="film-card__info">
            <div className="film-card__poster film-card__poster--big">
              <img src={film.src} alt={`${film.title} poster`} width="218"
                height="327"
              />
            </div>
            <Tabs/>
          </div>
        </div>
      </section>

      <div className="page-content">
        <section className="catalog catalog--like-this">
          <h2 className="catalog__title">More like this</h2>
          <FilmsList {...similarFilms}/>
        </section>
        <Footer />
      </div>
    </>
  );
}

export default MoviePage;
